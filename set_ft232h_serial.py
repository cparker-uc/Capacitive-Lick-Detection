"""Takes a serial number as argument on CLI and sets it to the connected
FTDI device (make sure there is only one plugged in when running this!"""

import sys
from pyftdi import eeprom

eeprom_ = eeprom.FtdiEeprom()
eeprom_.open('ftdi:///1')

eeprom_.dump_config()

eeprom_.set_serial_number(sys.argv[1] if sys.argv[1] else 'undefined')
eeprom_.commit(dry_run=False)

